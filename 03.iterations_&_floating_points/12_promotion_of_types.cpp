#include <iostream>
using namespace std;
int main()
{
    char c = 'A';
    cout<<" char c = "<<c<<endl;
    short k = c ;
    cout<<" short k = "<<k<<endl;
    int m = k ;
    cout<<" int m = "<<m<<endl;
    long n = m ;
    cout<<" long n = "<<n<<endl;
    float x = n ;
    cout<<" float x = "<<x<<endl;
    double y = x ;
    cout<<" double y = "<<y<<endl;
}