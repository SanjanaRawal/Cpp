#include <iostream>
using namespace std;
int main() {
    int m=0;
    cout<<"INCREMENT"<<endl;
    cout<<" m = "<<m<<endl;
    cout<<" m++ = "<<m++<<endl;
    cout<<" m = "<<m<<endl;
    cout<<" ++m = "<<++m<<endl;
    cout<<" m = "<<m<<endl;
    int n=10;
    cout<<"DECREMENT"<<endl;
    cout<<" n = "<<n<<endl;
    cout<<" n-- = "<<n--<<endl;
    cout<<" n = "<<n<<endl;
    cout<<" --n = "<<--n<<endl;
    cout<<" n = "<<n<<endl;
}